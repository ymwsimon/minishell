#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = s;
	while (ptr && i < n)
	{
		ptr[i] = (char)c;
		i++;
	}
	return (s);
}
